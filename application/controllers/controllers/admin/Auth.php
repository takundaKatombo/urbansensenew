				}
				
			} else {

				if(!$user){
					//$this->data['message'] = 'Forgot password link is expired. Please try again.';

					$this->session->set_flashdata("warning",'Forgot password link is expired. Please try again.'); 
				} else {
					$this->data['title'] = 'Reset Password';
					$this->data['email'] = $user->email;
					$this->data['id'] = $id;
					$this->data['code'] = $code;
					$this->load->view('admin/auth/reset_password',$this->data);
